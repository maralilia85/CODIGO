package ec.mil.armada.inv.treeTable;

import java.io.Serializable;
import java.math.BigInteger;

/**
 *
 * @author maria_rodriguez
 */
public class Estructura_bodega implements Serializable {

    private static final long serialVersionUID = 5729347327615575421L;

    private BigInteger clave;
    private String descripcion;
    private String tipo;
    private BigInteger nivel;
    private String estado;
    private String nombreEmpleado;

    public Estructura_bodega(final BigInteger clave, final String descripcion, final String tipo, final BigInteger nivel, final String estado,
        final String nombreEmpleado) {
        this.clave = clave;
        this.descripcion = descripcion;
        this.tipo = tipo;
        this.nivel = nivel;
        this.estado = estado;
        this.nombreEmpleado = nombreEmpleado;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(final String descripcion) {
        this.descripcion = descripcion;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(final String tipo) {
        this.tipo = tipo;
    }

    public BigInteger getNivel() {
        return nivel;
    }

    public void setNivel(final BigInteger nivel) {
        this.nivel = nivel;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(final String estado) {
        this.estado = estado;
    }

    public BigInteger getClave() {
        return clave;
    }

    public void setClave(BigInteger clave) {
        this.clave = clave;
    }

    public String getNombreEmpleado() {
        return nombreEmpleado;
    }

    public void setNombreEmpleado(String nombreEmpleado) {
        this.nombreEmpleado = nombreEmpleado;
    }

}
